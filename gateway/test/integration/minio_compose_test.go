// TestMinIO_NoBucketCredentialsHardcodedInCompose — AC2 security guard
//
// Given: docker-compose.yml is read
// When:  the content is inspected for hardcoded MinIO credentials
// Then:  no MINIO_ROOT_USER or MINIO_ROOT_PASSWORD env vars have literal values
//        (they must use secrets or environment variable references only)
//
// FAILS if developer accidentally hardcodes credentials in docker-compose.yml.
func TestMinIO_NoBucketCredentialsHardcodedInCompose(t *testing.T) {
	projectRoot := projectRootForMinio()
	composeFile := projectRoot + "docker-compose.yml"

	data, err := os.ReadFile(composeFile)
	if err != nil {
		t.Skipf("cannot read docker-compose.yml at %s: %v", composeFile, err)
	}

	content := string(data)

	// Check that MINIO_ROOT_USER and MINIO_ROOT_PASSWORD are not assigned literal values.
	// A hardcoded value would look like: MINIO_ROOT_USER: "somevalue" (not a file reference or _FILE suffix).
	// We allow: MINIO_ROOT_USER_FILE (reading from Docker secret file) or empty references.
	hardcodedPatterns := []string{
		"MINIO_ROOT_USER: minioadmin",
		"MINIO_ROOT_PASSWORD: minioadmin",
		"MINIO_ROOT_USER: admin",
		"MINIO_ROOT_PASSWORD: password",
	}

	for _, pattern := range hardcodedPatterns {
		if strings.Contains(content, pattern) {
			t.Errorf("AC2 FAIL: hardcoded MinIO credential found in docker-compose.yml: %q — "+
				"use Docker Secrets (MINIO_ROOT_USER_FILE) instead", pattern)
		}
	}

	// If MINIO_ROOT_USER appears without _FILE suffix and without a ${...} reference, it's suspicious.
	if strings.Contains(content, "MINIO_ROOT_USER:") && !strings.Contains(content, "MINIO_ROOT_USER_FILE") {
		if !strings.Contains(content, "MINIO_ROOT_USER: ${") {
			t.Logf("AC2 WARNING: MINIO_ROOT_USER found without _FILE suffix and without ${} reference — verify it uses Docker Secrets")
		}
	}

	t.Log("AC2 PASS: no obviously hardcoded MinIO credentials found in docker-compose.yml")
}
